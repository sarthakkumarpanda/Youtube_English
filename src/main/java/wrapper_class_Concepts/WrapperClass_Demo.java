package wrapper_class_Concepts;

public class WrapperClass_Demo {
	
	public static void main(String[] args) {
	
	int a = 100; //a is just a variable which is holding value 100
	System.out.println(a);
	
	Integer b = 200; //b is the object of Integer type which contains value 200
	System.out.println(b);
	
	}

}
